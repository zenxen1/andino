package com.sds.server.dto;

public class room {
	private int room_no;
	private String r_registtime;
	private int r_captain;
	private String r_title;
	
	public int getRoom_no() {
		return room_no;
	}
	public void setRoom_no(int room_no) {
		this.room_no = room_no;
	}
	public String getR_registtime() {
		return r_registtime;
	}
	public void setR_registtime(String r_registtime) {
		this.r_registtime = r_registtime;
	}
	public int getR_captain() {
		return r_captain;
	}
	public void setR_captain(int r_captain) {
		this.r_captain = r_captain;
	}
	public String getR_title() {
		return r_title;
	}
	public void setR_title(String r_title) {
		this.r_title = r_title;
	}
	
	

}
