package com.sds.server.dto;

public class Friend {
	
	private int friend_no;
	private int f_me;
	private int f_you;
	
	public int getFriend_no() {
		return friend_no;
	}
	public void setFriend_no(int friend_no) {
		this.friend_no = friend_no;
	}
	public int getF_me() {
		return f_me;
	}
	public void setF_me(int f_me) {
		this.f_me = f_me;
	}
	public int getF_you() {
		return f_you;
	}
	public void setF_you(int f_you) {
		this.f_you = f_you;
	}
	
	

}
